interface WordmarkLoaderProps {
  isLoading: boolean
}

export default function WordmarkLoader({ isLoading }: WordmarkLoaderProps) {
  const [frame, setFrame] = React.useState(0)
  const [blank, setBlank] = React.useState(false)

  React.useEffect(() => {
    if (!isLoading) return

    setFrame(0)
    setBlank(false)

    let showTimeout: ReturnType<typeof setTimeout>
    let blankTimeout: ReturnType<typeof setTimeout>
    let currentFrame = 0

    const tick = () => {
      setBlank(false)
      setFrame(currentFrame)

      showTimeout = setTimeout(() => {
        setBlank(true)
        blankTimeout = setTimeout(() => {
          currentFrame = (currentFrame + 1) % PART_BOXES.length
          tick()
        }, BLANK_DURATION)
      }, FRAME_DURATION)
    }

    tick()

    return () => {
      clearTimeout(showTimeout)
      clearTimeout(blankTimeout)
    }
  }, [isLoading])

  if (!isLoading) return null

  const [bx, by, bw, bh] = PART_BOXES[frame]
  const viewBox = `${bx} ${by} ${bw} ${bh}`

  return (
    <div className="page-loading-overlay">
      <svg
        viewBox={viewBox}
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        className="loader-frame"
        style={{ opacity: blank ? 0 : 1 }}
      >
        {PATHS.filter((p) => p.part === frame).map((p, i) => (
          <path key={i} d={p.d} stroke="#F0FAFA" strokeWidth={p.sw} />
        ))}
      </svg>
    </div>
  )
}
